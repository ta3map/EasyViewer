function idx = argmin(A, dim)
    % Функция argmin возвращает индекс минимального элемента массива A.
    % Если указано измерение dim, поиск производится вдоль этого измерения.
    %
    % Пример использования:
    %   idx = argmin(A);         % Для вектора или матрицы
    %   idx = argmin(A, 1);      % Поиск по первому измерению (столбцам)
    
    if nargin < 2
        % Если измерение не указано, используется первое ненулевое измерение
        [~, idx] = min(A);
    else
        [~, idx] = min(A, [], dim);
    end
end
