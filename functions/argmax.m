function idx = argmax(A, dim)
    % Функция argmax возвращает индекс максимального элемента массива A.
    % Если указано измерение dim, поиск производится вдоль этого измерения.
    %
    % Пример использования:
    %   idx = argmax(A);         % Для вектора или матрицы
    %   idx = argmax(A, 2);      % Поиск по второму измерению (строкам)
    
    if nargin < 2
        % Если измерение не указано, используется первое ненулевое измерение
        [~, idx] = max(A);
    else
        [~, idx] = max(A, [], dim);
    end
end
